# converting one Data type to another Data Type
x=56
print(x,type(x))

#converting to string
y=str(x)	#----------------> converting to string
print(y,type(y))

# converting to bool
z=bool(x)	#----------------> converting to bool
print(z,type(z))

# converting to bool
p=0
print(p,type(p))
q=bool(p)	#----------------> converting to bool
print(q,type(q))

# converting from flot to integer
sa=10.0023
print(sa)
print(int(sa))	#----------------> converting to flot to integer



#	bool(empty)=False
print(bool())
print(bool(None))
print(bool([]))
print(bool({}))

